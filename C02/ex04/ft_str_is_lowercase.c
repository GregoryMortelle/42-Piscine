int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
	{
		if (str[i] >= 97 && str[i] <= 122)
				i++;
		else
			return (0);
	}
	return (1);
}

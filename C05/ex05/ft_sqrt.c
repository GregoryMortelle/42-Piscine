int	ft_sqrt(int nb)
{
	long	i;
	long	nb2;

	nb2 = nb;
	if (nb2 <= 0)
		return (0);
	if (nb2 == 1)
		return (1);
	i = 2;
	if (nb2 >= 2)
	{
		while (i * i <= nb2)
		{
			if (i * i == nb2)
				return (i);
			i++;
		}
	}
	return (0);
}
